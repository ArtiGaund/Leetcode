class Solution {
  public:
    void printTriangle(int n) {
        // code here
        for(int i=0;i<n;i++)
        {
            for(char ch=('A'+n-1);ch>=('A'+n-1)-i;ch--) cout<<ch<<" ";
            cout<<endl;
        }
    }
};

